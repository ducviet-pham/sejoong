package com.tuvi.dao;

public class SeoInfo {
	private int seoId;
	private String keyword;
	public int getSeoId() {
		return seoId;
	}
	public void setSeoId(int seoId) {
		this.seoId = seoId;
	}
	public String getKeyword() {
		return keyword;
	}
	public void setKeyword(String keyword) {
		this.keyword = keyword;
	}
}
